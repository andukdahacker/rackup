/// Spacing tokens and tap target constants for the RackUp design system.
///
/// Based on an 8dp grid system with 6 named tokens.
abstract final class RackUpSpacing {
  /// Extra small spacing — 4dp.
  static const double spaceXs = 4;

  /// Small spacing — 8dp.
  static const double spaceSm = 8;

  /// Medium spacing — 16dp.
  static const double spaceMd = 16;

  /// Large spacing — 24dp.
  static const double spaceLg = 24;

  /// Extra large spacing — 32dp.
  static const double spaceXl = 32;

  /// Double extra large spacing — 48dp.
  static const double spaceXxl = 48;

  /// Minimum tap target size — 56x56dp.
  static const double minTapTarget = 56;

  /// Primary action button minimum height — 64dp, full-width.
  static const double primaryButtonHeight = 64;

  /// Standard border width for outlined buttons and dividers.
  static const double borderWidth = 1.5;
}
